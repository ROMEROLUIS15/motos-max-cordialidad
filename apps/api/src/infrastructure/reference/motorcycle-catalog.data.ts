export const MOTORCYCLE_CATALOG: MotorcycleCatalogEntry[] = Object.entries(BRANDS).flatMap(
  ([brand, models]) =>
    models.map(([model, yearFrom, yearTo]) => ({
      brand: brand.replace('_', ' '),
      model,
      yearFrom,
      yearTo,
    })),
);

export const MOTORCYCLE_BRANDS: string[] = [
  ...new Set(MOTORCYCLE_CATALOG.map((e) => e.brand)),
].sort();
